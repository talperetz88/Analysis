package Class;


public class CircleData {
	
	public Point upEdge;
	public Point middleEdge;
	public Point bottomEdge;
	public Point center;
	public double radius;
	
	public CircleData(Point upEdge, Point middleEdge, Point bottomEdge, Point center, double radius)
	{
		this.upEdge = upEdge;
		this.middleEdge = middleEdge;
		this.bottomEdge = bottomEdge;
		this.center = center;
		this.radius = radius;
	}

}
